package com.baidu.pojo;

public class Student {
    private Integer id;
    private String name;
    private String sex;
    private String suo;
    private String date;
    private String dang;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getSuo() {
        return suo;
    }

    public void setSuo(String suo) {
        this.suo = suo;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getDang() {
        return dang;
    }

    public void setDang(String dang) {
        this.dang = dang;
    }
}
